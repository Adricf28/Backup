# Parametro es una variable definida entre los parentesis de la funcion
# Argumento (arg) es el valor enviado a la funcion

def funcion_arg(nombre, apellido):  # Defines una variable (parametro)
    print('El nombre recibido es:', nombre)
    print('El apellido es:', apellido)

funcion_arg('Juan', 'Perez')  # Le das valor a dicha variable (argumento)
funcion_arg('Carla', 'Gomez')
